export function log(msg: string) { console.log(msg); }
export function ok(msg: string) { console.log(`  [OK] ${msg}`); }
export function warn(msg: string) { console.log(`  [WARN] ${msg}`); }
export function fail(msg: string) { console.log(`  [FAIL] ${msg}`); }
export function info(msg: string) { console.log(`  ${msg}`); }
export function title(msg: string) { console.log(`\n${msg}`); }
