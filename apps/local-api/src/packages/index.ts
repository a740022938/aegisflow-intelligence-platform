// ── Get Package Artifacts ────────────────────────────────────────────────────

export async function getPackageArtifacts(id: string) {
  const dbInstance = getDatabase();
  const pkg = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id);
  if (!pkg) {
    return { ok: false, error: `Package ${id} not found` };
  }

  const links = dbInstance.prepare(`
    SELECT mpa.*, a.name, a.artifact_type, a.status, a.path, a.file_size_bytes
    FROM model_package_artifacts mpa
    LEFT JOIN artifacts a ON mpa.artifact_id = a.id
    WHERE mpa.package_id = ?
    ORDER BY mpa.created_at
  `).all(id);

  return {
    ok: true,
    package_id: id,
    artifacts: links,
    total: links.length,
  };
}

