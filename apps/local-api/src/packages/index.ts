// ── Create ───────────────────────────────────────────────────────────────────

export async function createPackage(body: any) {
  const dbInstance = getDatabase();
  const validation = createPackageSchema.safeParse(body);
  if (!validation.success) {
    return { ok: false, error: validation.error.message };
  }
  const data = validation.data;
  const id = generateId();
  const nowStr = now();

  // Verify model exists
  const model = dbInstance.prepare('SELECT * FROM models WHERE model_id = ?').get(data.model_id);
  if (!model) {
    return { ok: false, error: `Model ${data.model_id} not found` };
  }

  // Fetch artifacts
  let artifacts: any[] = [];
  let totalSize = 0;
  if (data.artifact_ids && data.artifact_ids.length > 0) {
    for (const artId of data.artifact_ids) {
      const art = dbInstance.prepare('SELECT * FROM artifacts WHERE id = ?').get(artId);
      if (art) {
        artifacts.push(art);
        totalSize += (art as any).file_size_bytes || 0;
      }
    }
  }

  // Generate manifest
  const manifest = generateManifest(
    { id, package_name: data.package_name, package_version: data.package_version, model_id: data.model_id, created_at: nowStr },
    artifacts.map((a) => ({
      id: a.id,
      name: a.name,
      artifact_type: a.artifact_type,
      path: a.path,
      file_size_bytes: a.file_size_bytes,
    }))
  );

  // Generate release note
  const releaseNote = generateReleaseNoteFull(
    { id, package_name: data.package_name, package_version: data.package_version, created_at: nowStr },
    artifacts,
    model
  );

  // Insert package
  dbInstance.prepare(`
    INSERT INTO model_packages (
      id, model_id, package_name, package_version, status,
      artifact_ids_json, manifest_json, release_note, storage_path,
      file_size_bytes, created_at, updated_at
    ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
  `).run(
    id, data.model_id, data.package_name, data.package_version, 'draft',
    JSON.stringify(data.artifact_ids || []), JSON.stringify(manifest),
    data.release_note || releaseNote, data.storage_path,
    totalSize, nowStr, nowStr
  );

  // Insert package-artifact links
  for (const art of artifacts) {
    const linkId = generateId();
    dbInstance.prepare(`
      INSERT INTO model_package_artifacts (
        id, package_id, artifact_id, artifact_role, relative_path,
        checksum, file_size_bytes, created_at
      ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)
    `).run(
      linkId, id, art.id, 'primary', art.path || '',
      '', art.file_size_bytes || 0, nowStr
    );
  }

  return {
    ok: true,
    package: {
      id,
      model_id: data.model_id,
      package_name: data.package_name,
      package_version: data.package_version,
      status: 'draft',
      artifact_ids_json: data.artifact_ids || [],
      manifest_json: manifest,
      release_note: data.release_note || releaseNote,
      file_size_bytes: totalSize,
      created_at: nowStr,
    },
  };
}

// ── Update ───────────────────────────────────────────────────────────────────

export async function updatePackage(id: string, body: any) {
  const dbInstance = getDatabase();
  const existing = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id);
  if (!existing) {
    return { ok: false, error: `Package ${id} not found` };
  }

  const validation = updatePackageSchema.safeParse(body);
  if (!validation.success) {
    return { ok: false, error: validation.error.message };
  }
  const data = validation.data;

  const fields: string[] = [];
  const values: any[] = [];

  if (data.package_name !== undefined) { fields.push('package_name = ?'); values.push(data.package_name); }
  if (data.package_version !== undefined) { fields.push('package_version = ?'); values.push(data.package_version); }
  if (data.status !== undefined) { fields.push('status = ?'); values.push(data.status); }
  if (data.release_note !== undefined) { fields.push('release_note = ?'); values.push(data.release_note); }
  if (data.storage_path !== undefined) { fields.push('storage_path = ?'); values.push(data.storage_path); }

  if (data.artifact_ids !== undefined) {
    fields.push('artifact_ids_json = ?');
    values.push(JSON.stringify(data.artifact_ids));

    // Update package-artifact links
    dbInstance.prepare('DELETE FROM model_package_artifacts WHERE package_id = ?').run(id);
    for (const artId of data.artifact_ids) {
      const art = dbInstance.prepare('SELECT * FROM artifacts WHERE id = ?').get(artId);
      if (art) {
        const linkId = generateId();
        dbInstance.prepare(`
          INSERT INTO model_package_artifacts (
            id, package_id, artifact_id, artifact_role, relative_path,
            checksum, file_size_bytes, created_at
          ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        `).run(
          linkId, id, artId, 'primary', (art as any).path || '',
          '', (art as any).file_size_bytes || 0, now()
        );
      }
    }

    // Regenerate manifest
    const pkg = existing as any;
    const artifacts = data.artifact_ids.map((aid: string) => {
      const a = dbInstance.prepare('SELECT * FROM artifacts WHERE id = ?').get(aid);
      return a || { id: aid, name: 'Unknown', artifact_type: 'unknown' };
    });
    const manifest = generateManifest(pkg, artifacts);
    fields.push('manifest_json = ?');
    values.push(JSON.stringify(manifest));
  }

  if (fields.length === 0) {
    return { ok: true, package: existing };
  }

  fields.push('updated_at = ?');
  values.push(now());
  values.push(id);
  dbInstance.prepare(`UPDATE model_packages SET ${fields.join(', ')} WHERE id = ?`).run(...values);

  const updated = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id);
  return {
    ok: true,
    package: {
      ...updated,
      artifact_ids_json: parseJsonField((updated as any).artifact_ids_json, 'artifact_ids_json'),
      manifest_json: parseJsonField((updated as any).manifest_json, 'manifest_json'),
    },
  };
}

