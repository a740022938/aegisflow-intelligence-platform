// ── Build Package (change status to building -> ready) ──────────────────────

export async function buildPackage(id: string) {
  const dbInstance = getDatabase();
  const pkg = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id) as any;
  if (!pkg) {
    return { ok: false, error: `Package ${id} not found` };
  }

  if (pkg.status !== 'draft') {
    return { ok: false, error: `Package must be in draft status to build (current: ${pkg.status})` };
  }

  // Update status to building
  dbInstance.prepare('UPDATE model_packages SET status = ?, updated_at = ? WHERE id = ?').run('building', now(), id);

  try {
    // Get artifacts
    const artifactIds = parseJsonField(pkg.artifact_ids_json, 'artifact_ids_json') as string[];
    const artifacts: any[] = [];
    let totalSize = 0;

    for (const artId of artifactIds) {
      const art = dbInstance.prepare('SELECT * FROM artifacts WHERE id = ?').get(artId) as any;
      if (art) {
        artifacts.push(art);
        totalSize += art.file_size_bytes || 0;
      }
    }

    // Generate manifest
    const manifest = {
      package: {
        id: pkg.id,
        name: pkg.package_name,
        version: pkg.package_version,
        model_id: pkg.model_id,
        created_at: pkg.created_at,
      },
      artifacts: artifacts.map((a: any) => ({
        id: a.id,
        name: a.name,
        type: a.artifact_type,
        format: a.format,
        framework: a.framework,
        path: a.path,
        size_bytes: a.file_size_bytes,
        size_mb: a.file_size_bytes ? Math.round(a.file_size_bytes / 1024 / 1024 * 100) / 100 : 0,
      })),
      metadata: {
        generated_at: now(),
        generator: 'AGI Factory v1.6.0',
        total_size_bytes: totalSize,
        total_size_mb: Math.round(totalSize / 1024 / 1024 * 100) / 100,
        artifact_count: artifacts.length,
      },
    };

    // Generate release note
    const releaseNote = generateReleaseNote(pkg, artifacts);

    // Create staging directory
    const fs = await import('fs/promises');
    const path = await import('path');
    const crypto = await import('crypto');

    const stagingDir = path.join(process.cwd(), 'packages', 'storage', 'staging', id);
    const releaseDir = path.join(process.cwd(), 'packages', 'storage', 'releases', id);

    // Ensure directories exist
    await fs.mkdir(stagingDir, { recursive: true });
    await fs.mkdir(releaseDir, { recursive: true });

    // Write manifest.json
    const manifestPath = path.join(releaseDir, 'manifest.json');
    await fs.writeFile(manifestPath, JSON.stringify(manifest, null, 2), 'utf-8');

    // Write release-note.md
    const releaseNotePath = path.join(releaseDir, 'release-note.md');
    await fs.writeFile(releaseNotePath, releaseNote, 'utf-8');

    // Generate checksums
    const checksums: { file: string; sha256: string; size: number }[] = [];

    // Add manifest checksum
    const manifestContent = await fs.readFile(manifestPath);
    const manifestHash = crypto.createHash('sha256').update(manifestContent).digest('hex');
    checksums.push({ file: 'manifest.json', sha256: manifestHash, size: manifestContent.length });

    // Add release note checksum
    const rnContent = await fs.readFile(releaseNotePath);
    const rnHash = crypto.createHash('sha256').update(rnContent).digest('hex');
    checksums.push({ file: 'release-note.md', sha256: rnHash, size: rnContent.length });

    // Process artifacts (copy/link files if they exist)
    const artifactsDir = path.join(releaseDir, 'artifacts');
    await fs.mkdir(artifactsDir, { recursive: true });

    for (const art of artifacts) {
      if (art.path) {
        // Try to find the actual file
        const possiblePaths = [
          art.path,
          path.join(process.cwd(), art.path),
          path.join(process.cwd(), 'models', art.path),
          path.join(process.cwd(), 'output', art.path),
        ];

        let sourceFile: string | null = null;
        for (const p of possiblePaths) {
          try {
            await fs.access(p);
            sourceFile = p;
            break;
          } catch {
            // File not found at this path
          }
        }

        if (sourceFile) {
          // Copy file to release directory
          const destPath = path.join(artifactsDir, `${art.name.replace(/[^a-zA-Z0-9._-]/g, '_')}.${art.format || 'bin'}`);
          await fs.copyFile(sourceFile, destPath);

          // Calculate checksum
          const fileContent = await fs.readFile(destPath);
          const fileHash = crypto.createHash('sha256').update(fileContent).digest('hex');
          checksums.push({
            file: path.basename(destPath),
            sha256: fileHash,
            size: fileContent.length,
          });

          // Update artifact link with checksum
          dbInstance.prepare(`
            UPDATE model_package_artifacts 
            SET checksum = ?, file_size_bytes = ?, relative_path = ?
            WHERE package_id = ? AND artifact_id = ?
          `).run(fileHash, fileContent.length, path.basename(destPath), id, art.id);
        }
      }
    }

    // Write checksums.json
    const checksumsPath = path.join(releaseDir, 'checksums.json');
    await fs.writeFile(checksumsPath, JSON.stringify(checksums, null, 2), 'utf-8');

    // Calculate overall package checksum
    const packageHash = crypto.createHash('sha256')
      .update(JSON.stringify(checksums))
      .digest('hex');

    // Update package in database
    const storagePath = `apps/local-api/packages/storage/releases/${id}`;
    dbInstance.prepare(`
      UPDATE model_packages 
      SET status = ?, manifest_json = ?, release_note = ?, storage_path = ?,
          file_size_bytes = ?, checksum = ?, updated_at = ?
      WHERE id = ?
    `).run(
      'ready',
      JSON.stringify(manifest),
      releaseNote,
      storagePath,
      totalSize,
      packageHash,
      now(),
      id
    );

    // Audit: build success
    await logAudit({
      category: 'package',
      action: 'build',
      target: `${pkg.package_name}@${pkg.package_version}`,
      result: 'success',
      detail: {
        package_id: id,
        model_id: pkg.model_id,
        files: checksums.length,
        total_size_mb: Math.round(totalSize / 1024 / 1024 * 100) / 100,
        files_created: ['manifest.json', 'release-note.md', 'checksums.json']
      }
    });

    const updated = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id);
    return {
      ok: true,
      package: {
        ...updated,
        artifact_ids_json: parseJsonField((updated as any).artifact_ids_json, 'artifact_ids_json'),
        manifest_json: parseJsonField((updated as any).manifest_json, 'manifest_json'),
      },
      build_result: {
        storage_path: storagePath,
        files: checksums,
        total_size_bytes: totalSize,
        total_size_mb: Math.round(totalSize / 1024 / 1024 * 100) / 100,
      },
    };
  } catch (error: any) {
    // Mark as failed
    dbInstance.prepare('UPDATE model_packages SET status = ?, updated_at = ? WHERE id = ?').run('failed', now(), id);
    await logAudit({
      category: 'package',
      action: 'build',
      target: `${pkg.package_name}@${pkg.package_version}`,
      result: 'failed',
      detail: { package_id: id, error: error.message }
    });
    return {
      ok: false,
      error: `Build failed: ${error.message}`,
      package: pkg,
    };
  }
}

// ── Publish Package ──────────────────────────────────────────────────────────

export async function publishPackage(id: string) {
  const dbInstance = getDatabase();
  const pkg = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id) as any;
  if (!pkg) {
    return { ok: false, error: `Package ${id} not found` };
  }

  if (pkg.status !== 'ready') {
    return { ok: false, error: `Package must be built (status=ready) before publishing (current: ${pkg.status})` };
  }

  try {
    const fs = await import('fs/promises');
    const path = await import('path');

    // Verify release files exist
    const releaseDir = path.join(process.cwd(), 'packages', 'storage', 'releases', id);
    const manifestPath = path.join(releaseDir, 'manifest.json');
    const checksumsPath = path.join(releaseDir, 'checksums.json');

    try {
      await fs.access(manifestPath);
      await fs.access(checksumsPath);
    } catch {
      return { ok: false, error: 'Release files not found. Please rebuild the package.' };
    }

    // Verify checksums
    const checksumsContent = await fs.readFile(checksumsPath, 'utf-8');
    const checksums = JSON.parse(checksumsContent);

    const verifiedFiles: string[] = [];
    const failedFiles: string[] = [];

    for (const entry of checksums) {
      const filePath = path.join(releaseDir, entry.file);
      try {
        await fs.access(filePath);
        verifiedFiles.push(entry.file);
      } catch {
        failedFiles.push(entry.file);
      }
    }

    if (failedFiles.length > 0) {
      await logAudit({
        category: 'package',
        action: 'publish',
        target: `${pkg.package_name}@${pkg.package_version}`,
        result: 'failed',
        detail: { package_id: id, reason: 'missing_files', files: failedFiles }
      });
      return {
        ok: false,
        error: `Missing files: ${failedFiles.join(', ')}`,
      };
    }

    // Update status to published
    const publishedAt = now();
    dbInstance.prepare(`
      UPDATE model_packages 
      SET status = ?, updated_at = ?
      WHERE id = ?
    `).run('published', publishedAt, id);

    // Write publish record
    const publishRecord = {
      package_id: id,
      published_at: publishedAt,
      verified_files: verifiedFiles,
      checksum_count: checksums.length,
    };

    const publishRecordPath = path.join(releaseDir, 'publish-record.json');
    await fs.writeFile(publishRecordPath, JSON.stringify(publishRecord, null, 2), 'utf-8');

    // Audit: publish success
    await logAudit({
      category: 'package',
      action: 'publish',
      target: `${pkg.package_name}@${pkg.package_version}`,
      result: 'success',
      detail: {
        package_id: id,
        model_id: pkg.model_id,
        verified_files: verifiedFiles.length,
        consistency_check: {
          manifest_json: true,
          checksums_json: true,
          all_artifacts: verifiedFiles.length === checksums.length
        },
        published_at: publishedAt,
        storage_path: pkg.storage_path
      }
    });

    const updated = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id);
    return {
      ok: true,
      package: {
        ...updated,
        artifact_ids_json: parseJsonField((updated as any).artifact_ids_json, 'artifact_ids_json'),
        manifest_json: parseJsonField((updated as any).manifest_json, 'manifest_json'),
      },
      publish_result: {
        published_at: publishedAt,
        verified_files: verifiedFiles.length,
        storage_path: pkg.storage_path,
      },
    };
  } catch (error: any) {
    await logAudit({
      category: 'package',
      action: 'publish',
      target: `${pkg.package_name}@${pkg.package_version}`,
      result: 'failed',
      detail: { package_id: id, error: error.message }
    });
    return {
      ok: false,
      error: `Publish failed: ${error.message}`,
    };
  }
}

// ── Helper: Generate Release Note (2-param, for buildPackage) ─────────────────

function generateReleaseNote(pkg: any, artifacts: any[]): string {
  const lines: string[] = [];
  const totalSize = artifacts.reduce((sum, a) => sum + (a.file_size_bytes || 0), 0);

  lines.push(`# ${pkg.package_name} v${pkg.package_version}`);
  lines.push('');
  lines.push(`**Published:** ${now()}`);
  lines.push(`**Model ID:** ${pkg.model_id}`);
  lines.push(`**Total Size:** ${Math.round(totalSize / 1024 / 1024 * 100) / 100} MB`);
  lines.push('');
  lines.push('## Contents');
  lines.push('');

  artifacts.forEach((a: any, i: number) => {
    lines.push(`${i + 1}. **${a.name}** (${a.artifact_type})`);
    if (a.framework) lines.push(`   - Framework: ${a.framework}`);
    if (a.format) lines.push(`   - Format: ${a.format}`);
    if (a.file_size_bytes) {
      lines.push(`   - Size: ${Math.round(a.file_size_bytes / 1024 / 1024 * 100) / 100} MB`);
    }
    lines.push('');
  });

  lines.push('## Checksums');
  lines.push('');
  lines.push('See `checksums.json` for SHA256 hashes of all files.');
  lines.push('');
  lines.push('---');
  lines.push('*Generated by AGI Factory*');

  return lines.join('\n');
}

// ── Retry Operations ─────────────────────────────────────────────────────────

export async function retryPackageBuild(id: string): Promise<any> {
  const dbInstance = getDatabase();
  const pkg = dbInstance.prepare('SELECT * FROM model_packages WHERE id = ?').get(id) as any;
  if (!pkg) return { ok: false, error: `Package ${id} not found` };

  const lastFailure = dbInstance.prepare(
    `SELECT * FROM audit_logs WHERE category = 'package' AND action = 'build' AND target LIKE ? AND result = 'failed' ORDER BY created_at DESC LIMIT 1`
  ).get(`%${pkg.package_name}%`);

  const retryId = await logAudit({
    category: 'package',
    action: 'retry_build',
    target: `${pkg.package_name}@${pkg.package_version}`,
    result: 'partial',
    detail: {
      package_id: id,
      model_id: pkg.model_id,
      original_failure: lastFailure ? {
        at: lastFailure.created_at,
        error: lastFailure.detail_json ? JSON.parse(String(lastFailure.detail_json)).error : 'unknown'
      } : null
    }
  });

  const result = await buildPackage(id);

  const detailUpdate = {
    ...(lastFailure ? { original_failure: JSON.parse(String(lastFailure.detail_json || '{}')) } : {}),
    retry_result: result.ok ? 'build succeeded' : result.error
  };
  dbInstance.prepare(
    `UPDATE audit_logs SET result = ?, detail_json = ? WHERE id = ?`
  ).run(result.ok ? 'success' : 'failed', JSON.stringify(detailUpdate), retryId);

  return result;
}

