// 动态导入避免循环依赖
async function logAudit(params: { category: string; action: string; target: string; result: 'success' | 'failed' | 'partial'; detail?: Record<string, any> }) {
  try {
    const audit = await import('../audit/index.js');
    return await audit.logAudit(params);
  } catch {
    // 审计模块不可用时静默忽略
  }
}

function generateId() {
  return crypto.randomUUID();
}

function now() {
  return new Date().toISOString();
}

function parseJsonField(val: string | undefined | null, fieldName: string) {
  if (!val) return {};
  try {
    return JSON.parse(val);
  } catch {
    throw new Error(`Invalid JSON in ${fieldName}`);
  }
}

// ── Schemas ──────────────────────────────────────────────────────────────────

const createPackageSchema = z.object({
  model_id: z.string().min(1, 'model_id is required'),
  package_name: z.string().min(1, 'package_name is required'),
  package_version: z.string().default('1.0.0'),
  artifact_ids: z.array(z.string()).default([]),
  release_note: z.string().default(''),
  storage_path: z.string().default(''),
});

const updatePackageSchema = z.object({
  package_name: z.string().optional(),
  package_version: z.string().optional(),
  status: z.enum(['draft', 'building', 'ready', 'published', 'archived', 'failed']).optional(),
  artifact_ids: z.array(z.string()).optional(),
  release_note: z.string().optional(),
  storage_path: z.string().optional(),
});

// ── Helper: Generate Manifest ───────────────────────────────────────────────

function generateManifest(pkg: any, artifacts: any[]): object {
  return {
    package: {
      id: pkg.id,
      name: pkg.package_name,
      version: pkg.package_version,
      model_id: pkg.model_id,
      created_at: pkg.created_at,
    },
    artifacts: artifacts.map((a) => ({
      id: a.id,
      name: a.name,
      type: a.artifact_type,
      role: a.role || 'primary',
      path: a.relative_path || a.path || '',
      size: a.file_size_bytes || 0,
      checksum: a.checksum || '',
    })),
    metadata: {
      generated_at: now(),
      generator: 'AegisFlow Intelligence Platform v1.5.0',
    },
  };
}

// ── Helper: Generate Release Note ───────────────────────────────────────────

function generateReleaseNoteFull(pkg: any, artifacts: any[], model: any): string {
  const lines: string[] = [];
  lines.push(`# ${pkg.package_name} v${pkg.package_version}`);
  lines.push('');
  lines.push(`**Model:** ${model?.name || 'Unknown'} (${model?.version || 'N/A'})`);
  lines.push(`**Created:** ${fmt(pkg.created_at)}`);
  lines.push('');
  lines.push('## Contents');
  lines.push('');
  artifacts.forEach((a, i) => {
    lines.push(`${i + 1}. **${a.name}** (${a.artifact_type})`);
    if (a.file_size_bytes) {
      lines.push(`   - Size: ${formatBytes(a.file_size_bytes)}`);
    }
  });
  lines.push('');
  lines.push('---');
  lines.push(`*Generated by AegisFlow Intelligence Platform*`);
  return lines.join('\n');
}

function formatBytes(bytes?: number | null) {
  if (!bytes) return '—';
  if (bytes < 1024) return bytes + ' B';
  if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(1) + ' KB';
  if (bytes < 1024 * 1024 * 1024) return (bytes / (1024 * 1024)).toFixed(1) + ' MB';
  return (bytes / (1024 * 1024 * 1024)).toFixed(2) + ' GB';
}

function fmt(s?: string | null) {
  if (!s) return '—';
  try { return new Date(s).toLocaleString('zh-CN'); } catch { return s; }
}

// ── List ─────────────────────────────────────────────────────────────────────

export async function listPackages(query: any) {
  const dbInstance = getDatabase();
  const { model_id, status, q } = query;

  let sql = 'SELECT * FROM model_packages WHERE 1=1';
  const params: any[] = [];

  if (model_id) {
    sql += ' AND model_id = ?';
    params.push(model_id);
  }
  if (status) {
    sql += ' AND status = ?';
    params.push(status);
  }
  if (q) {
    sql += ' AND (package_name LIKE ? OR package_version LIKE ?)';
    params.push(`%${q}%`, `%${q}%`);
  }

  sql += ' ORDER BY created_at DESC';

  const rows = dbInstance.prepare(sql).all(...params);
  return {
    ok: true,
    packages: rows.map((r: any) => ({
      ...r,
      artifact_ids_json: parseJsonField(r.artifact_ids_json, 'artifact_ids_json'),
      manifest_json: parseJsonField(r.manifest_json, 'manifest_json'),
    })),
    total: rows.length,
  };
}

